package survey;

/**
 * 설문 응답자 정보 객체
 * @author cheeeeze
 *
 */
public class SurveyUserInfo {

	
	private int age;
	private String location;
	
	public int getAge() {
		return age;
	}
	
	public String getLocation() {
		return location;
	}
	
	public void setAge( int age ) {
		this.age = age;
	}
	
	public void setLocation( String location ) {
		this.location = location;
	}
}
